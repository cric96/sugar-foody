      <p>Dalla pagina fattorini può registrare un nuovo fattorino, modificarlo o eliminarlo.
         Le credenziali devono essere assegnate da lei, per poi essere modificate dal fattorino dopo aver
         effettuato l'accesso.
      </p>
      <h3>Un mio cliente / fattorino ha perso le sue credenziali, cosa posso fare?</h3>
      <p>Contattaci, risolveremo assieme il problema.
      </p>
      <h3>Come capisco se un prodotto è stato al mio listino?</h3>
      <p>Il prodotto sarà presente nella pagina listino.
      </p>
