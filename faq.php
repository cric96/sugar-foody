      <h2>Domande frequenti: cliente</h2>
      <h3>Come posso ordinare?</h3>
      <p>
        Una volta esserti registrato ed aver effettuato il login con le tue credenziali, potrai effettuare
        un ordine scegliendo, tra la lista di ristoranti che ti troverai davanti, quello da cui vuoi ordinare.
        <span id="attention">Ricorda</span>, puoi ordinare da un solo ristorante per ordine.
      </p>
      <h3>Una volta scelto il ristornate come procedo?</h3>
      <p>Non appena avrai scelto il ristorante da cui ordinare, verrai indirizzato ad una pagina
        che ti mostrerà quali sono le categorie di prodotti che puoi prendere presso quel fornitore.
        E' possibile ordinare diverse categorie di prodotti in uno stesso ordine, purché
        siano dello stesso ristorante.
      </p>
      <h3>Come capisco se un prodotto è stato aggiunto al mio carrello?</h3>
      <p>Una volta scelto il ristorante e la categoria di prodotto da ordinare, ti verrà mostrata una
        lista di prodotti disponibili presso quel fornitore. Una volta scelto il prodotto, per aggiungerlo
        al carrello ti basterà cliccare sul carrellino sulla linea del prodotto da te scelto e successivamente
        sul tasto "conferma" a destra della finestra che ti comparirà.
      </p>
    </section>
