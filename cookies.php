      <h2>Cookies</h2>
      <h3>Cosa sono i cookie tecnici</h3>
      <p>I cookie tecnici sono quelli utilizzati al solo fine di “effettuare la trasmissione di una comunicazione
        su una rete di comunicazione elettronica, o nella misura strettamente necessaria al fornitore di un servizio
        della società dell’informazione esplicitamente richiesto dall’abbonato o dall’utente a erogare tale servizio”
        (cfr. art. 122, comma 1, del Codice).
        Essi non vengono utilizzati per scopi ulteriori e sono normalmente installati direttamente dal titolare o
        gestore del sito web. Possono essere suddivisi in cookie di navigazione o di sessione, che garantiscono la
        normale navigazione e fruizione del sito web (permettendo, ad esempio, di realizzare un acquisto o
        autenticarsi per accedere ad aree riservate); cookie analytics, assimilati ai cookie tecnici laddove
        utilizzati direttamente dal gestore del sito per raccogliere informazioni, in forma aggregata, sul numero
        degli utenti e su come questi visitano il sito stesso; cookie di funzionalità, che permettono all’utente la
        navigazione in funzione di una serie di criteri selezionati (ad esempio, la lingua, i prodotti selezionati
        per l’acquisto) al fine di migliorare il servizio reso allo stesso.
        Per l’installazione di tali cookie non è richiesto il preventivo consenso degli utenti, mentre resta fermo
        l’obbligo di dare l’informativa ai sensi dell’art. 13 del Codice, che il gestore del sito, qualora utilizzi
        soltanto tali dispositivi, potrà fornire con le modalità che ritiene più idonee.”</p>
      <h3>Cookie utilizzati nel sito</h3>
      <p>Non viene fatto uso di cookies per la trasmissione di informazioni di carattere personale,
          né vengono utilizzati c.d. cookies persistenti di alcun tipo, ovvero sistemi per il
          tracciamento degli utenti. L’uso di c.d. cookies di sessione (che non vengono
          memorizzati in modo persistente sul computer dell’utente e svaniscono con la chiusura
          del browser) è strettamente limitato alla trasmissione di identificativi di sessione
          (costituiti da numeri casuali generati dal server) necessari per consentire l’esplorazione
          sicura ed efficiente del sito. I c.d. cookies di sessione utilizzati in questo sito evitano il
          ricorso ad altre tecniche informatiche potenzialmente pregiudizievoli per la riservatezza
          della navigazione degli utenti e non consentono l’acquisizione di dati personali
          identificativi dell’utente.</p>
